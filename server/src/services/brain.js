// Главный экспортный метод
export function answer(question) {
  // Очищаем от знаков препинания и бьем на слова
  const tokens = question.toLowerCase().replace(/[?!.,]/g, '').split(' ');
  
  let bestMatch = null;
  let highestScore = 0;

  // Прогоняем каждое слово из вопроса через базу знаний
  for (const entry of KNOWLEDGE_BASE) {
    let score = 0;
    
    for (const keyword of entry.keywords) {
      for (const token of tokens) {
        // Если слово длиннее 4 букв, прощаем 2 опечатки (ошибки микрофона)
        if (token.length > 4 && getEditDistance(token, keyword) <= 2) {
          score += 3; // Сильное совпадение с учетом искажений
        } 
        // Для коротких слов или точного вхождения корня
        else if (token.includes(keyword) || keyword.includes(token)) {
          score += 2;
        }
      }
    }

    if (score > highestScore) {
      highestScore = score;
      bestMatch = entry;
    }
  }

  // Порог срабатывания: если набрали хотя бы 2 балла — выдаем умный ответ
  if (bestMatch && highestScore >= 2) {
    return bestMatch.content;
  }

  // Если нейронка не нашла пересечений (защита от "сколько стоят гвозди")
  return FALLBACKS[Math.floor(Math.random() * FALLBACKS.length)];
}